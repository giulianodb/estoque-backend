package br.org.demaosunidas.dto;

public class PedagogicoDashDTO {
	private Integer totalInscricao;
	
	private Integer totalInscricaoManha;
	
	private Integer totalInscricaoTarde;
	
	private Integer totalEspera;

	public Integer getTotalInscricao() {
		return totalInscricao;
	}

	public void setTotalInscricao(Integer totalInscricao) {
		this.totalInscricao = totalInscricao;
	}

	public Integer getTotalInscricaoManha() {
		return totalInscricaoManha;
	}

	public void setTotalInscricaoManha(Integer totalInscricaoManha) {
		this.totalInscricaoManha = totalInscricaoManha;
	}

	public Integer getTotalInscricaoTarde() {
		return totalInscricaoTarde;
	}

	public void setTotalInscricaoTarde(Integer totalInscricaoTarde) {
		this.totalInscricaoTarde = totalInscricaoTarde;
	}

	public Integer getTotalEspera() {
		return totalEspera;
	}

	public void setTotalEspera(Integer totalEspera) {
		this.totalEspera = totalEspera;
	}
	
}
